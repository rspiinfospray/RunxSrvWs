package org.infospray.runx.model;

public class LapSpeedFixing {
	
	private double maxSpeed;
	
	private double avg;
	
	private long startLapTime;
	
	private long stopLapTime;

	public double getMaxSpeed() {
		return maxSpeed;
	}

	public void setMaxSpeed(double maxSpeed) {
		this.maxSpeed = maxSpeed;
	}

	public double getAvg() {
		return avg;
	}

	public void setAvg(double avg) {
		this.avg = avg;
	}

	public long getStartLapTime() {
		return startLapTime;
	}

	public void setStartLapTime(long startLapTime) {
		this.startLapTime = startLapTime;
	}

	public long getStopLapTime() {
		return stopLapTime;
	}

	public void setStopLapTime(long stopLapTime) {
		this.stopLapTime = stopLapTime;
	}
	
	

}
